package Lab_4;
public class Parquet extends WoodenProducts {
	public Parquet(String name, String material, String manufacturer, double price, double width, double length,
			double thicknes, int amount) {
		setName(name);
		setMaterial(material);
		setManufacturer(manufacturer);
		setThicknes(thicknes);
		setWidth(width);
		setLength(length);
		setPrice(price);
		setAmount(amount);

	}
}
